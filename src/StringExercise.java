public class StringExercise {
    public static void main(String[] args) {
        String weDontNeedNo = "We don't need no education\nWe don't need no thought control";
        System.out.println(weDontNeedNo);

        String escape = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(escape);

        String cDrive = "In windows, the main drive is usually C:\\";
        System.out.println(cDrive);

        String escapingSlash = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(escapingSlash);
    }
}
